morse = {
  ".-": "a",
  "-...": "b",
  "-.-.": "c",
  "-..": "d",
  ".": "e",
  "..-.": "f",
  "--.": "g",
  "....": "h",
  "..": "i",
  ".---": "j",
  "-.-": "k",
  ".-..": "l",
  "--": "m",
  "-.": "n",
  "---": "o",
  ".--.": "p",
  "--.-": "q",
  ".-.": "r",
  "...": "s",
  "-": "t",
  "..-": "u",
  "...-": "v",
  ".--": "w",
  "-..-": "x",
  "-.--": "y",
  "--..": "z",
};

/**방법 1 */
function solution(letter) {
  let result = "";
  letter.split(" ").forEach((item) => (result += morse[item]));
  return result;
}

/**방법 2 */
function solution(letter) {
  return letter.split(" ").reduce((prev, curr) => prev + morse[curr], "");
}
